#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
  * op_add - Adds
  * @a: num one
  * @b: num two
  * Return: 0
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - performs subtraction
  * @a: num one
  * @b: num two
  * Return: 0
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - performs multiplication
  * @a: num one
  * @b: num two
  * Return: 0
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - performs division
  * @a: num one
  * @b: num two
  * Return: 0
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - to find modulus
  * @a: num one
  * @b: num two
  * Return: 0
  */
int op_mod(int a, int b)
{
	return (a % b);
}
